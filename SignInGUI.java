import javafx.application.Application;
import javafx.geometry.Insets;
import javafx.scene.Scene;
import javafx.scene.control.*;
import javafx.scene.layout.GridPane;
import javafx.stage.Stage;

public class SignInGUI extends Application {

    @Override
    public void start(Stage primaryStage) {
        primaryStage.setTitle("SignIn Page");

        GridPane grid = new GridPane();
        grid.setPadding(new Insets(10, 10, 10, 10));
        grid.setVgap(5);
        grid.setHgap(5);

        Label usernameLabel = new Label("Username:");
        GridPane.setConstraints(usernameLabel, 0, 0);
        TextField usernameField = new TextField();
        GridPane.setConstraints(usernameField, 1, 0);

        Label passwordLabel = new Label("Password:");
        GridPane.setConstraints(passwordLabel, 0, 1);
        PasswordField passwordField = new PasswordField();
        GridPane.setConstraints(passwordField, 1, 1);

        Button signInButton = new Button("Sign In");
        GridPane.setConstraints(signInButton, 1, 2);
        signInButton.setOnAction(e -> {
            SignIn signIn = new SignIn();
            signIn.signIn(usernameField.getText(), passwordField.getText());
        });

        grid.getChildren().addAll(usernameLabel, usernameField, passwordLabel, passwordField, signInButton);

        Scene scene = new Scene(grid, 300, 150);
        primaryStage.setScene(scene);
        primaryStage.show();
    }

    public static void main(String[] args) {
        launch(args);
    }
}